<?PHP 
    public class User(){
        private $idUser;
        private $nome;
        private $login;
        private $senha;
        
        public function __construct($nome, $login, $senha){
            $this nome -> $nome;
            $this login -> $login;
            $this senha -> $senha;
        }
        
        public function getId(){
            return $this -> $idUser;
        }
        
        public function getNome(){
            return nome -> $nome;
        }
        
        public function getLogin(){
            return login -> $login;
        }
        
        public function getSenha(){
            return senha -> $senha;
        }
    }
?>